						inflow of persons from affected countries, Kerala state has
						strengthened the surveillance and control measures against the
						disease and has gone forward in identifying almost all possible
						contacts thereby preventing spread of disease and timely health
						interventions to affected persons. State level, District level and
						Local Self Government Institution level structures have been set
						up in the state to ensure coordinated response to COVID 19
						management. Interdepartmental coordination at district level to
						handle issues and spreading of information, education and
						communication related to COVID 19 is spearheaded by the District
						Administration.
					</p>
					<p style="text-align: justify;">
						<span style="margin-left: 3em;"></span><b>CovidCare4U</b> is a
						comprehensive solution for effective daily reporting/monitoring of
						the quarantine and health status of the people under surveillance
						by Field Health Workers like PHN/JHI/Asha (Ward RRT) and provision
						of health service through tele consultation and referral by
						Medical Officers based on the reports available in the system.
						This portal is designed for disseminating information related to
						Covid 19 for public and provisions for accessing emergency
						services for public. This platform can be used to generate
						simplified daily report on welfare measures initiated by the
						District Administration for public who are provided with support
						during lock down like support to elderly people, migrant
						labourers, Public Distribution system. <span style="color: blue">This
						is also a one stop platform for the public to avail emergency
						services and information related to Covid19 and ensures
						transparency and quality in public service and welfare measures.</span>
						This Covid19 information and management solution is Conceptualized and designed by <b>Anurag A</b>,
